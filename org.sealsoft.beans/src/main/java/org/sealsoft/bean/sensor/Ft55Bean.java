package org.sealsoft.bean.sensor;

public class Ft55Bean extends SensorBean {
	private static final long serialVersionUID = -1929865788924141414L;
	private String linksoben = "";
	private String linksunten = "";
	private String rechtsoben = "";
	private String rechtsunten = "";

	public String getLinksoben() {
		return linksoben;
	}

	public void setLinksoben(String linksoben) {
		this.linksoben = linksoben;
	}

	public String getLinksunten() {
		return linksunten;
	}

	public void setLinksunten(String linksunten) {
		this.linksunten = linksunten;
	}

	public String getRechtsoben() {
		return rechtsoben;
	}

	public void setRechtsoben(String rechtsoben) {
		this.rechtsoben = rechtsoben;
	}

	public String getRechtsunten() {
		return rechtsunten;
	}

	public void setRechtsunten(String rechtsunten) {
		this.rechtsunten = rechtsunten;
	}

}
